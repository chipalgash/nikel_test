def validate_test_data() -> bool:
    """
    Проверяет корректность тестовых данных.
    
    Returns:
        bool: True если данные валидны
        
    Raises:
        ValueError: При несоответствии количества вопросов и ответов
    """
    if len(QUESTIONS) != len(ANSWERS):
        raise ValueError(
            f"Количество вопросов ({len(QUESTIONS)}) не соответствует "
            f"количеству ответов ({len(ANSWERS)})"
        )
    
    # Проверяем наличие пустых вопросов или ответов
    for i, (question, answer) in enumerate(zip(QUESTIONS, ANSWERS)):
        if not question.strip():
            raise ValueError(f"Пустой вопрос в позиции {i}")
        if not answer.strip():
            raise ValueError(f"Пустой ответ в позиции {i}")
    
    return True


def get_test_statistics() -> dict:
    """
    Возвращает статистику по тестовым данным.
    
    Returns:
        dict: Словарь со статистикой
    """
    return {
        "total_questions": len(QUESTIONS),
        "total_answers": len(ANSWERS),
        "avg_question_length": sum(len(q) for q in QUESTIONS) / len(QUESTIONS),
        "avg_answer_length": sum(len(a) for a in ANSWERS) / len(ANSWERS),
        "longest_question": max(QUESTIONS, key=len),
        "longest_answer": max(ANSWERS, key=len),
    }


def print_test_overview() -> None:
    """
    Выводит обзор тестовых данных в консоль.
    """
    print("📋 ОБЗОР ТЕСТОВЫХ ДАННЫХ")
    print("=" * 50)
    
    stats = get_test_statistics()
    print(f"Всего вопросов: {stats['total_questions']}")
    print(f"Средняя длина вопроса: {stats['avg_question_length']:.1f} символов")
    print(f"Средняя длина ответа: {stats['avg_answer_length']:.1f} символов")
    
    print("\n🔍 ПРИМЕРЫ ВОПРОСОВ:")
